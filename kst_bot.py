                "–í—ã–±–µ—Ä–∏—Ç–µ —Ä–∞–∑–¥–µ–ª –≤ –º–µ–Ω—é –ö–°–¢ ‚ö°Ô∏è –∏–ª–∏ –∫–æ–º–∞–Ω–¥—É /menu.",
